function studentMatches(student, query) {
  const normalizedQuery = query.toLowerCase().trim();
  if (!normalizedQuery) return true;

  const values = [
    student.name,
    student.roll,
    student.fullRoll,
    student.room,
    student.phone,
    student.fatherPhone,
    student.email,
    student.address,
    student.blood,
    student.group,
    student.section,
    student.practicalGroup,
    student.college,
    student.bn?.name,
    student.bn?.address,
    student.bn?.college
  ].filter(Boolean).join(" ").toLowerCase();

  return values.includes(normalizedQuery);
}

function getActiveFilters() {
  return {
    classNo: byId("filterClass")?.value || "",
    group: byId("filterGroup")?.value || "",
    section: byId("filterSection")?.value || "",
    blood: byId("filterBlood")?.value || ""
  };
}

function studentPassesFilters(student, filters = getActiveFilters()) {
  if (filters.classNo && student.classNo !== filters.classNo) return false;
  if (filters.group && student.group !== filters.group) return false;
  if (filters.section && student.section !== filters.section) return false;
  if (filters.blood && student.blood !== filters.blood) return false;
  return true;
}

function getFilteredStudents() {
  const search = byId("studentSearch");
  const query = search?.value || "";
  const filters = getActiveFilters();
  return students.filter((student) => studentPassesFilters(student, filters) && studentMatches(student, query));
}


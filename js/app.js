function initAos() {
  if (!window.AOS) {
    document.querySelectorAll("[data-aos]").forEach((element) => {
      element.removeAttribute("data-aos");
      element.style.opacity = "1";
      element.style.transform = "none";
    });
    return;
  }

  window.AOS.init({
    duration: 900,
    once: true,
    offset: 80,
    easing: "ease-out-cubic"
  });
}

